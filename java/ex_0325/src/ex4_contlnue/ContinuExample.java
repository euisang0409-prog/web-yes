package ex4_contlnue;

public class ContinuExample {

	public static void main(String[] args) {
		// forนฎภว ฐๆฟ์ continueธฆ ธธณชฐิ ตวธ้ ม๕ฐจฝฤภธทฮ ภฬตฟ
		for(int i = 1; i <= 10; ) {
			if(i % 3 == 0) {
				continue;
			}
			System.out.println(i+" ");
			i++;
		}
		
		// while, do-whileภว ฐๆฟ์ มถฐวฝฤภธทฮ ภฬตฟวัดู.
		
		
		
		
		
		
	}
}
